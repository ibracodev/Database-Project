    private void getNewData(){
       
        try {
            
          rs = dbCon.executeStatement("select distinct id from managers, teams where id not in (select mgr_id from teams)");        
            
          cmbManagerID.removeAllItems();
          while (rs.next()) {
                cmbManagerID.addItem(rs.getString("id"));
            }
             
             rs.beforeFirst();
             rs.first();
              populateFields();
            } catch (SQLException e) {
                System.out.println(e);
            }
    }
    
    private void populateFields() {
        try {
              cmbManagerID.setSelectedItem(rs.getString("id"));
              
        } catch (SQLException ex) {
            Logger.getLogger(UpdateDeleteManager.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

