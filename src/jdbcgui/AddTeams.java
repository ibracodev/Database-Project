    private void btnAddTeamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddTeamActionPerformed
        // TODO add your handling code here:
        
        
        try {
            
            if (isValidData()) {
                String prepSQL = "INSERT INTO teams (team_id, team_name, mgr_id, home_ground) VALUES ( "
                + txtTeamID.getText().trim() + " ,"
                +"'"+txtTeamName.getText().trim() + "',"
                +cmbManagerID.getSelectedItem().toString() +","
                + "'"+ txtHomeGround.getText().trim() + "')";
                
                int result = dbCon.executePrepared(prepSQL);
                if (result > 0) {

                    javax.swing.JLabel label = new javax.swing.JLabel("New team added successfully.");
                    label.setFont(new java.awt.Font("Arial", java.awt.Font.BOLD, 18));
                    JOptionPane.showMessageDialog(null, label, "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
                    getNewData();
                    clearInputBoxes();
                } else {
                    // check validation errors 
                }

                rs.close();
            } else {

                javax.swing.JLabel label = new javax.swing.JLabel("Please fix validation errors...");
                label.setFont(new java.awt.Font("Arial", java.awt.Font.BOLD, 18));
                JOptionPane.showMessageDialog(null, label, "ERROR", JOptionPane.ERROR_MESSAGE);

            }  
        }
        catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error adding new team." + e.getMessage());       
        }      
    }//GEN-LAST:event_btnAddTeamActionPerformed

